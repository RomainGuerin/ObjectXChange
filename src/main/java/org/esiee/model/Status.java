package org.esiee.model;

/**
 * Enum representing the status of an entity.
 */
public enum Status {
    /**
     * Indicates that the entity has been accepted.
     */
    Accepted,

    /**
     * Indicates that the entity is pending approval.
     */
    Pending,

    /**
     * Indicates that the entity has been denied.
     */
    Denied
}